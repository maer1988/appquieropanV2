package com.example.appquieropan.Entidad;

public class SubProducto {

    private String id_subProducto;
    private String desc_subProducto;
    private String id_producto;

    public SubProducto() {
    }

    public String getId_subProducto() {
        return id_subProducto;
    }

    public void setId_subProducto(String id_subProducto) {
        this.id_subProducto = id_subProducto;
    }

    public String getDesc_subProducto() {
        return desc_subProducto;
    }

    public void setDesc_subProducto(String desc_subProducto) {
        this.desc_subProducto = desc_subProducto;
    }

    public String getId_producto() {
        return id_producto;
    }

    public void setId_producto(String id_producto) {
        this.id_producto = id_producto;
    }
}
