package com.example.appquieropan.Entidad;

public class Promocion {

    private String id_promocion;
    private String desc_promocion;
    private String tipo_promocion;
    private String tipo_descuento;
    private String tipo_publicidad;
    private String tipo_accion;
    private String fecha_inicion;
    private String fecha_fin;

    public Promocion() {
    }

    public String getId_promocion() {
        return id_promocion;
    }

    public void setId_promocion(String id_promocion) {
        this.id_promocion = id_promocion;
    }

    public String getDesc_promocion() {
        return desc_promocion;
    }

    public void setDesc_promocion(String desc_promocion) {
        this.desc_promocion = desc_promocion;
    }

    public String getTipo_promocion() {
        return tipo_promocion;
    }

    public void setTipo_promocion(String tipo_promocion) {
        this.tipo_promocion = tipo_promocion;
    }

    public String getTipo_descuento() {
        return tipo_descuento;
    }

    public void setTipo_descuento(String tipo_descuento) {
        this.tipo_descuento = tipo_descuento;
    }

    public String getTipo_publicidad() {
        return tipo_publicidad;
    }

    public void setTipo_publicidad(String tipo_publicidad) {
        this.tipo_publicidad = tipo_publicidad;
    }

    public String getTipo_accion() {
        return tipo_accion;
    }

    public void setTipo_accion(String tipo_accion) {
        this.tipo_accion = tipo_accion;
    }

    public String getFecha_inicion() {
        return fecha_inicion;
    }

    public void setFecha_inicion(String fecha_inicion) {
        this.fecha_inicion = fecha_inicion;
    }

    public String getFecha_fin() {
        return fecha_fin;
    }

    public void setFecha_fin(String fecha_fin) {
        this.fecha_fin = fecha_fin;
    }
}
