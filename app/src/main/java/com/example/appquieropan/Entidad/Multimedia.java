package com.example.appquieropan.Entidad;

public class Multimedia {

    private String id_multi;
    private String desc_multi;
    private String producto;

    public Multimedia() {
    }

    public String getId_multi() {
        return id_multi;
    }

    public void setId_multi(String id_multi) {
        this.id_multi = id_multi;
    }

    public String getDesc_multi() {
        return desc_multi;
    }

    public void setDesc_multi(String desc_multi) {
        this.desc_multi = desc_multi;
    }

    public String getProducto() {
        return producto;
    }

    public void setProducto(String producto) {
        this.producto = producto;
    }
}
