package com.example.appquieropan.Entidad;

public class Valoracion {

    private String IDvalorado;
    private String rut_valorado;
    private String Idusuario;
    private String IDvoucher;
    private String Valoracion;


    public Valoracion() {
    }

    public String getIDvalorado() {
        return IDvalorado;
    }

    public void setIDvalorado(String IDvalorado) {
        this.IDvalorado = IDvalorado;
    }

    public String getRut_valorado() {
        return rut_valorado;
    }

    public void setRut_valorado(String rut_valorado) {
        this.rut_valorado = rut_valorado;
    }

    public String getIdusuario() {
        return Idusuario;
    }

    public void setIdusuario(String idusuario) {
        Idusuario = idusuario;
    }

    public String getIDvoucher() {
        return IDvoucher;
    }

    public void setIDvoucher(String IDvoucher) {
        this.IDvoucher = IDvoucher;
    }

    public String getValoracion() {
        return Valoracion;
    }

    public void setValoracion(String valoracion) {
        Valoracion = valoracion;
    }
}
