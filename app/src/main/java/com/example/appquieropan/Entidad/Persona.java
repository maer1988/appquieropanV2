package com.example.appquieropan.Entidad;

public class Persona {

    private String id_persona;
    private String nom_persona;
    private String apat_persona;
    private String amat_persona;
    private String sexo_persona;
    private int edad_persona;
    private String cod_identi_persona;

    public Persona() {
    }

    public String getId_persona() {
        return id_persona;
    }

    public void setId_persona(String id_persona) {
        this.id_persona = id_persona;
    }

    public String getNom_persona() {
        return nom_persona;
    }

    public void setNom_persona(String nom_persona) {
        this.nom_persona = nom_persona;
    }

    public String getApat_persona() {
        return apat_persona;
    }

    public void setApat_persona(String apat_persona) {
        this.apat_persona = apat_persona;
    }

    public String getAmat_persona() {
        return amat_persona;
    }

    public void setAmat_persona(String amat_persona) {
        this.amat_persona = amat_persona;
    }

    public String getSexo_persona() {
        return sexo_persona;
    }

    public void setSexo_persona(String sexo_persona) {
        this.sexo_persona = sexo_persona;
    }

    public int getEdad_persona() {
        return edad_persona;
    }

    public void setEdad_persona(int edad_persona) {
        this.edad_persona = edad_persona;
    }

    public String getCod_identi_persona() {
        return cod_identi_persona;
    }

    public void setCod_identi_persona(String cod_identi_persona) {
        this.cod_identi_persona = cod_identi_persona;
    }
}
