package com.example.appquieropan.Entidad;

public class Perfil {

    private String id_perfil;
    private String desc_perfil;
    private String fechaIni_perfil;
    private String fechaMod_perfil;
    private String estado_perfil;

    public Perfil() {
    }

    public String getId_perfil() {
        return id_perfil;
    }

    public void setId_perfil(String id_perfil) {
        this.id_perfil = id_perfil;
    }

    public String getDesc_perfil() {
        return desc_perfil;
    }

    public void setDesc_perfil(String desc_perfil) {
        this.desc_perfil = desc_perfil;
    }

    public String getFechaIni_perfil() {
        return fechaIni_perfil;
    }

    public void setFechaIni_perfil(String fechaIni_perfil) {
        this.fechaIni_perfil = fechaIni_perfil;
    }

    public String getFechaMod_perfil() {
        return fechaMod_perfil;
    }

    public void setFechaMod_perfil(String fechaMod_perfil) {
        this.fechaMod_perfil = fechaMod_perfil;
    }

    public String getEstado_perfil() {
        return estado_perfil;
    }

    public void setEstado_perfil(String estado_perfil) {
        this.estado_perfil = estado_perfil;
    }
}
